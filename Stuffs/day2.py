from flask import Flask, request, render_template, redirect, url_for
import smtplib
from email.mime.multipart import MIMEMultipart
from email.mime.text import MIMEText
from email.mime.base import MIMEBase
from email import encoders
import os

app = Flask(__name__)

@app.route('/')
def index():
    return render_template('index.html')

@app.route('/send-email', methods=['POST'])
def send_email():
    recipient = request.form['recipient']
    cc_emails = request.form['cc']
    files = request.files.getlist('files')
    
    # Save files temporarily
    file_paths = []
    for file in files:
        file_path = os.path.join('uploads', file.filename)
        file.save(file_path)
        file_paths.append(file_path)
    
    # Email configuration
    sender_email = "arunvijo2004@gmail.com"
    password = "eddc reto iscy uqwl"  # Use the App Password generated by Google

    # Create the email
    message = MIMEMultipart()
    message["From"] = sender_email
    message["To"] = recipient
    message["Subject"] = "Excel File Attachments"

    # Add CC recipients
    if cc_emails:
        message["Cc"] = cc_emails
        cc_list = cc_emails.split(',')
    else:
        cc_list = []

    # Email body
    body = "Please find the attached Excel files."
    message.attach(MIMEText(body, "plain"))

    # Attach files
    for file_path in file_paths:
        with open(file_path, "rb") as attachment:
            part = MIMEBase("application", "octet-stream")
            part.set_payload(attachment.read())
            encoders.encode_base64(part)
            part.add_header(
                "Content-Disposition",
                f"attachment; filename= {os.path.basename(file_path)}",
            )
            message.attach(part)

    # Send the email
    try:
        with smtplib.SMTP_SSL("smtp.gmail.com", 465) as server:
            server.login(sender_email, password)
            server.sendmail(sender_email, [recipient] + cc_list, message.as_string())
        print("Email sent successfully!")
    except Exception as e:
        print(f"Error: {e}")
    finally:
        for file_path in file_paths:
            os.remove(file_path)  # Remove the files after sending
    
    return redirect(url_for('index'))

if __name__ == '__main__':
    if not os.path.exists('uploads'):
        os.makedirs('uploads')
    app.run(debug=True)
